package oops_1;

public class StaticandNonstatic {

	/*
	 * Static method & Variable : we can call both static var and methods through
	 * the class name with .operator,
	 * 
	 * Non static method & variable: for calling non static we need to create object
	 * 
	 * We can create multiple objects
	 */

	// global static variable
	public static int q = 200;

	public int p = 100; // global non static variable

	// static method
	public static void first_function() {
		int abc = 20; // local variable

	}

	/* non static method */

	public void second_function() {

	}

}
